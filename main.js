// Add 
function add(stored_value, input) {
    let result = stored_value + input;
    return result;
}

// Subtract
function subtract(stored_value,input) {
    let result = stored_value - input;
    return result;
}

// Multiply
function mutiply(stored_value, input) {
    let result = stored_value * input;
    return result;
}

// Divide
function divide(stored_value, input) {
    let result = stored_value / input;
    return result;
}


